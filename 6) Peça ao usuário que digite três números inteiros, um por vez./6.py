# Lista de n√∫meros
numeros = [2, 4, 6, 8, 10]


numeros_dobrados = [n * 2 for n in numeros]

# mostra as duas listas
print("Lista original:", numeros)
print("Lista dobrada :", numeros_dobrados)


soma_original = sum(numeros)
soma_dobrada = sum(numeros_dobrados)

# mostra os resultados
print("\nSoma da lista original:", soma_original)
print("Soma da lista dobrada :", soma_dobrada)
